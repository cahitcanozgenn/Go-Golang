package functions

func Topla(number1 int, number2 int) int {
	var toplam = number1 + number2
	return toplam
}

func Cikar(number1 int, number2 int) int {
	var fark = number1 - number2
	return fark
}

func Carp(number1 int, number2 int) int {
	var carpim = number1 * number2
	return carpim
}

func Bol(number1 int, number2 int) int {
	var bolum = number1 / number2
	return bolum
}
